    private void jBContinuarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBContinuarActionPerformed
        try {
            String clave = new String(jPFClave.getPassword());
            if (clave.length() >= 6) {
                controlador.crearUsuario(Long.parseLong(jLIdUsuario.getText()),
                        clave);
                setBounds(0, 6, 492, 340);
                jBCrear.setEnabled(true);
                jTFFiltroBusqueda.setEditable(true);
                jTUsuarios.setEnabled(true);
                jLIdUsuario.setText("");
                jPFClave.setText("");
                jTFFiltroBusqueda.setText("");
                listarUsuariosNoRegistrados("");
            } else {
                JOptionPane.showMessageDialog(null, "La clave debe ser mayor a 5 caracteres");
                jPFClave.setText("");
            }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_jBContinuarActionPerformed

