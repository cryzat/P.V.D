    public void ponerNombreBoton(String estado) {
        switch (estado) {
            case "Bloqueado":
                jButton1.setText("Desbloquear");
                break;
            case "Desbloqueado":
                jButton1.setText("Bloquear");
                break;
        }
    }

    public void listarUsuariosNoRegistrados(String parametro) {
        try {
            List<Persona> usuarios = controlador.listarUsuarioRegistrados(parametro);
            DefaultTableModel tabla = (DefaultTableModel) jTUsuarios.getModel();
            tabla.setRowCount(0);
            for (Persona usuario : usuarios) {
                Object[] fila = new Object[5];
                fila[0] = usuario.getNumeroDocumento();
                fila[2] = usuario.getNombre();
                fila[3] = usuario.getApellido();
                if (usuario instanceof Estudiante) {
                    fila[1] = "Estudiante";
                } else if (usuario instanceof Docente) {
                    fila[1] = "Docente";
                } else if (usuario instanceof Director) {
                    fila[1] = "Director";
                }
                if (usuario.isBloqueado()) {
                    fila[4] = "Bloqueado";
                } else {
                    fila[4] = "Desbloqueado";
                }
                tabla.addRow(fila);
            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }
